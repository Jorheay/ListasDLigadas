package ListasD;


public class informacion {

    private int x;
    private int z;

    public informacion(int elemento) {
        this.x = elemento;
        this.z = elemento;
    }

    public int getInformacion() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }
    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
